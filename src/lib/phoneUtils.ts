const sortedDialCodes = Array.from(dialCodeSet).sort(
  (a, b) => b.length - a.length
);

export const getCountryDialCode = (countryCode?: string): string => {
  if (!countryCode) return DEFAULT_DIAL_CODE;
  return dialCodeMap[countryCode.toUpperCase()]?.trim() || DEFAULT_DIAL_CODE;
};

export const getAllDialCodes = () => sortedDialCodes;
